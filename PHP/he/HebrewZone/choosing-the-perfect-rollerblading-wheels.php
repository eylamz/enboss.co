        <ul>
            <li><strong class="strongSubs">גלגלים רכים (78A-84A)</strong><br> מתאימים לרכיבה בחוץ, כיוון שהם סופגים זעזועים ומספקים אחיזה מעולה על משטחים מחוספסים. גלגלים רכים הם אידיאליים למתחילים המעדיפים יציבות.</li>
            <li><strong class="strongSubs">גלגלים בינוניים  (84A-88A)</strong><br> מספקים שילוב בין אחיזה לעמידות. מתאימים לסוגי רכיבה שונים הכוללים כושר, פנאי ורכיבה עירונית.</li>
            <li><strong class="strongSubs">גלגלים קשיחים  (+88A)</strong><br> מותאמים בעיקר לרכיבה על משטחים חלקים כגון אולמות או סקייטפארקים. הם מספקים פחות אחיזה אבל מצוינים למהירות גבוהה וביצוע טריקים והחלקות שונות (סליידים). גלגלים אלו נפוצים יותר ברכיבה על רולר אגרסיב.
