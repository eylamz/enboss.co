        <h3><strong>1.</strong> גודל הגלגל</h3>
          <p>גודל הגלגל משפיע ישירות על המהירות, יכולת התמרון והיציבות של הרולרבליידס. כאן נחלק את גדלי הגלגלים הנפוצים וההשלכות שלהם:</p>
