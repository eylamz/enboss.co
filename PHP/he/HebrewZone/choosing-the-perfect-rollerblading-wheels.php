                <li><strong class="strongSubs">גלגלים בינוניים (80-90 ממ)</strong><br> טובים לרכיבה ברמה גבוהה לצורך כושר. מספקים שילוב בין מהירות ויכולת תמרון, מה שמציע רכיבה חלקה יותר על משטחים קשיחים.</li>
                <li><strong class="strongSubs">גלגלים גדולים (+90-110 ממ)</strong><br> מותאמים לצורך כושר ומרחקים ארוכים. מאפשרים מהירות גבוהה מאוד ושיווי משקל על משטחים שאינם ישרים. ככל שהגלגלים גדולים יותר, כך הם שומרים על המומנטום (תנופה) בצורה טובה יותר ומספקים יציבות במהירויות גבוהות.</li>
